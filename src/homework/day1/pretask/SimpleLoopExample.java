// чтобы вывести числа от 1 до этой переменной;

package homework.day1.pretask;

public class SimpleLoopExample {

    public static void main(String[] args) {
        int number = 9;
        for (int i = 1; i <= number; i++){
            System.out.println(i);
        }
    }
}